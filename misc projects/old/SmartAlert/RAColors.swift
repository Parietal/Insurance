import Foundation
import UIKit

class RAColors:NSObject
{
    
    class func LIGHT_GRAY() -> UIColor {return UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 1.0) }
    class func TAN_GRAY() -> UIColor {return UIColor(red: 250/255, green: 249/255, blue: 248/255, alpha: 1.0) }
    class func GRAY() -> UIColor {return UIColor(red: 229/255, green: 229/255, blue: 229/255, alpha: 1.0) }
    class func MEDIUM_GRAY() -> UIColor {return UIColor(red: 153/255, green: 153/255, blue: 153/255, alpha: 1.0) }
    class func DARK_GRAY() -> UIColor {return UIColor(red: 72/255, green: 73/255, blue: 76/255, alpha: 1.0) }
    class func LIGHT_BLUE() -> UIColor {return UIColor(red: 76/255, green: 188/255, blue: 232/255, alpha: 1.0) }
    class func ORANGE() -> UIColor {return UIColor(red: 255/255, green: 102/255, blue: 51/255, alpha: 1.0) }
    
}